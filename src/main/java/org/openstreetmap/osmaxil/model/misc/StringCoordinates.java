package org.openstreetmap.osmaxil.model.misc;

public class StringCoordinates {
    
    public StringCoordinates() {
    }
    
    public StringCoordinates(String x, String y, String z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
    public String x;
    
    public String y;
    
    public String z;

}
