package org.openstreetmap.osmaxil.model;

public class Coordinates {
    
    public Coordinates() {
    }
    
    public Coordinates(String x, String y, String z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
    public String x;
    
    public String y;
    
    public String z;

}
