package org.openstreetmap.osmaxil.model;

public class Coordinates {
    
    public Coordinates() {
    }
    
    public Coordinates(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
    public float x;
    
    public float y;
    
    public float z;

}
